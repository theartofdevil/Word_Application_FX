package sample;

public class Noun {
    int id;
    String articleNoun , wordNoun , pluralNoun , meaningNoun ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getArticleNoun() {
        return articleNoun;
    }

    public void setArticleNoun(String articleNoun) {
        this.articleNoun = articleNoun;
    }

    public String getWordNoun() {
        return wordNoun;
    }

    public void setWordNoun(String wordNoun) {
        this.wordNoun = wordNoun;
    }

    public String getPluralNoun() {
        return pluralNoun;
    }

    public void setPluralNoun(String pluralNoun) {
        this.pluralNoun = pluralNoun;
    }

    public String getMeaningNoun() {
        return meaningNoun;
    }

    public void setMeaningNoun(String meaningNoun) {
        this.meaningNoun = meaningNoun;
    }
}
